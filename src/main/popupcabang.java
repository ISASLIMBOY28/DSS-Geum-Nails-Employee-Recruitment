    protected void datatable(){
 Object[] Baris ={"ID Cabang", "Nama Cabang"};
 tabmode = new DefaultTableModel(null, Baris);
 String find=cari.getText();
 try {
 String sql = "SELECT id_cbg, nm_cbg FROM cabang where id_cbg like '%"+find+"%' or nm_cbg like '%"+find+"%' order by id_cbg asc";
 java.sql.Statement stat = conn.createStatement();
 ResultSet hasil = stat.executeQuery(sql);
 while (hasil.next()){
 tabmode.addRow(new Object[]{
 hasil.getString(1),
 hasil.getString(2)
 });
 }
 tablecabang1.setModel(tabmode);
 } catch (Exception e) {
     JOptionPane.showMessageDialog(null, "Error table popup cabang: "+e);
 }
}

