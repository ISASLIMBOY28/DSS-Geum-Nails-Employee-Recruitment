    public void datacabang(){
       try{
            String sql = "select nm_cbg,kota,almt_cbg,telp_cbg  from lowongan, cabang where lowongan.id_low='"+idlow.getText()+"' and lowongan.id_cbg=cabang.id_cbg";
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
            cbg = hasil.getString("nm_cbg");
            kt = hasil.getString("kota");
            almt = hasil.getString("almt_cbg");
            telp= hasil.getString("telp_cbg");
            }
            System.out.println(cbg);
            System.out.println(kt);
            System.out.println(almt);
            System.out.println(telp);
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error data cabang: "+e);
        }
    }
