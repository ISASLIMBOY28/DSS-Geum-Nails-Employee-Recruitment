    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //print laporan
        try{
            Date date = new Date(System.currentTimeMillis());
        SimpleDateFormat tgl = new SimpleDateFormat("EEEEE d MMMM yyyy", new Locale("id"));
   String tngl = tgl.format(date);
   datacabang();
       if (jns.getSelectedItem().equals("Calon Karyawan")) {
           namaFile = "./src/report/lapcalonkaryawan.jasper";
           jumlahcalonkywn();
        }else if (jns.getSelectedItem().equals("Penilaian")){
           namaFile = "./src/report/lapnilai.jasper";
           jumlahnilai();
        }else if (jns.getSelectedItem().equals("Ranking")){
           namaFile = "./src/report/lapranking.jasper";
           jumlahrank();
        }else if (jns.getSelectedItem().equals("Karyawan Baru")){
           namaFile = "./src/report/lapkaryawanbaru.jasper";
           jumlahkbaru();
        }
            HashMap hash = new HashMap();
            BufferedImage image = ImageIO.read(getClass().getResource("/img/geum tengah transparan.png"));
            hash.put("logo", image);
            hash.put("lwg", idlow.getText());
            hash.put("pg", adm);
            hash.put("today", tngl);
            hash.put("jml",jml);
            hash.put("cbg",cbg);
            hash.put("kota",kt);
            hash.put("alamat",almt);
            hash.put("telp",telp);
            hash.put("lvl",lvl);
            JasperPrint jasperPrint = JasperFillManager.fillReport(namaFile, hash, conn);
            JasperViewer.viewReport(jasperPrint, false);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Error Report: "+e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

